#10) დაბეჭდეთ 1-დან 100-მდე მხოლოდ ის რიცხვები რომლებიც 3-ის ჯერადიც არის და 5-ის ჯერადიც

i = 1

while i < 101:
    if i % 3 == 0:
        print(str(i)  + "." " 3-ის ჯერადია")
    elif i % 5 == 0:
        print(str(i) + "." "5-ის ჯერადია")
    else:
        print(str(i) + "." "არ არის ჯერადი")
    i+=1